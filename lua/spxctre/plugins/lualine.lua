            lualine_y = { 'progress' },
            lualine_z = { 'location' }
        },
        inactive_sections = {
            lualine_a = {},
            lualine_b = {},
            lualine_c = { 'filename' },
            lualine_x = { 'location' },
            lualine_y = {},
            lualine_z = {}
        },
        tabline = {},
        winbar = {},
        inactive_winbar = {},
        extensions = {}
    },
    config = function (_, opts)
        local lualine = require('lualine')

        lualine.setup(opts)

        vim.opt.showmode = false
        vim.g.screenkey_statusline_component = true

    end,
}
